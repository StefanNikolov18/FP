{-29.1. “Двойно-линейна функция” наричаме функция f :: Double → Double, ко-
ято има стойност a1x за x ≤ 0 и стойност a2x за x > 0. Да се дефинира
функция dlin::Double -> Double-> (Double->Double), която по аргу-
менти a1 и a2, създава съответната двойно-линейна функция. Функцията
на фигура 4.1 може да се създаде чрез dlin (-1) 1.-}


dlin :: Double -> Double -> (Double -> Double)
dlin a1 a2 = \x -> if x <= 0 then a1*x else a2*x