{-29.5. Да се дефинира функция member::[a]->(a->bool). Стойността на фун-
кцията за даден списък l е функция над a, която проверява дали a е
елемент на l. На следния пример функцията myf има стойност истина за
всички четни числа и лъжа за останалите.
myf = member [ 2 , 4 , . . ]-}

member :: Eq a => [a] -> (a -> Bool)
member l = \x -> x `elem` l

myf :: Integer -> Bool
myf = member [2,4..1000]